package com.flipkart.domain;
	public class Product {
		private int id;
		private String prodType;
		private String prodComp;
		private double prodPrice;
		private String prodModal;
		private int prodAvailable;
		private String prodImg;
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getProdType() {
			return prodType;
		}
		public void setProdType(String prodType) {
			this.prodType = prodType;
		}
		public String getProdComp() {
			return prodComp;
		}
		public void setProdComp(String prodComp) {
			this.prodComp = prodComp;
		}
		public double getProdPrice() {
			return prodPrice;
		}
		public void setProdPrice(double prodPrice) {
			this.prodPrice = prodPrice;
		}
		public String getProdModal() {
			return prodModal;
		}
		public void setProdModal(String prodModal) {
			this.prodModal = prodModal;
		}
		public int getProdAvailable() {
			return prodAvailable;
		}
		public void setProdAvailable(int prodAvailable) {
			this.prodAvailable = prodAvailable;
		}
		public String getProdImg() {
			return prodImg;
		}
		public void setProdImg(String prodImg) {
			this.prodImg = prodImg;
		}
		
	}



